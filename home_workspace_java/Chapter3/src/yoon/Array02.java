package yoon;

public class Array02 {
  public static void main(String[] args) {
    int[] arr = new int[3];
    arr[0] = 7; // 배열 0번째 요소에 7을 저장하여라.
    arr[1] = 8;
    arr[2] = 9;
    int num = arr[0] + arr[1] + arr[2]; // arr 배열의 0, 1, 2번째 요소에 저장된 값을 num에 저장하겠다.(같은 자료형이니 가능)
    System.out.println(num);

  }
}
