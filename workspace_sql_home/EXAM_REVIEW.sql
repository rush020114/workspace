SELECT 
	EMPNO
	, ENAME
	, SAL
	, COMM AS 인센티브
FROM emp
WHERE SAL >= 500
AND SAL <= 3000;

SELECT
	EMPNO
	, ENAME
	, JOB
	, HIREDATE
FROM emp
WHERE ENAME 
LIKE '%병%'
OR ENAME
LIKE '%김%'
ORDER BY
	JOB ASC
	, EMPNO DESC;
	
SELECT 
	EMPNO
	, ENAME
	, DEPTNO
	, CASE DEPTNO
		WHEN 10 THEN '인사부'
		WHEN 20 THEN '영업부'
		WHEN 30 THEN '개발부'
		ELSE '생산부' END AS 부서명
FROM emp;

SELECT
	EMPNO
	, ENAME
	, TO_CHAR(HIREDATE, 'YYYY.MM.DD HH24.MI.SS') AS HIREDATE
FROM emp
ORDER BY HIREDATE ASC; 

SELECT
	EMPNO 
	, ENAME
	, SAL
	, COMM
	, SAL * 12 + IFNULL(COMM, 0) AS TOTAL_SALARY
FROM emp
ORDER BY TOTAL_SALARY DESC;

