public class HelloWorld {
  public static void main(String[] args){
    System.out.println("Hello World!"); // "Hello World!"라는 문자를 출력하라라는 뜻
    System.out.println("Hello"); //"Hello"를 한 줄 개행하여 출력
    System.out.println("World");
    System.out.println("Java");
    System.out.print("Hello "); // "Hello"를 개행하지 않고 출력
    System.out.print("World ");
    System.out.print("Java "); // 위의 3개를 개행하지 않고 출력하므로 "Hello World Java"가 출력됨
    System.out.println("Hello");
    // System.out.println("World"); //주석이어서 결과의 내용을 처리하지 않음

  }
}
