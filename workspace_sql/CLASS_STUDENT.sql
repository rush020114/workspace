	STU_NUM INT PRIMARY KEY AUTO_INCREMENT
	, STU_NAME VARCHAR(20) NOT NULL
	, STU_AGE INT 
	, CLASS_NUM INT REFERENCES CLASS_INFO (CLASS_NUM)
