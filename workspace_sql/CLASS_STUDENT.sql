# 외래키(Foreign key)
# 테이블 간의 관계 표현에 사용. 다른 테이블의 기본키를 참조!
# 외래키는 종속되는 테이블에 주고 부모테이블에 없는 데이터를 변형을 막는다.

# 게시판 테이블 (부모 테이블)
CREATE TABLE QNA_BOARD(
	BOARD_NUM INT PRIMARY KEY
	, TITLE VARCHAR(20)
	, CONTENT VARCHAR(20)
	, WRITER VARCHAR(20)
);

# 댓글 테이블 (자식 테이블)
CREATE TABLE REPLY (
	REPLY_NUM INT PRIMARY KEY
	, REPLY_CONTENT VARCHAR(20)
	, REPLY_WRITER VARCHAR(20)
	, BOARD_NUM INT REFERENCES QNA_BOARD (BOARD_NUM) ON DELETE CASCADE # 부모 테이블의 데이터 삭제 시 함께 삭제
);

