public class OperatorTest3 {
  public static void main(String[] args){
    // 비교연산자 : 참/거짓을 판단할 때 사용
    int score1 = 10;
    int score2 = 3;

    System.out.println(score1 > score2); // 1이 2보다 큰가
    System.out.println(score1 >= score2); // 1이 2보다 크거나 같은가
    System.out.println(score1 < score2); // 1이 2보다 작은가
    System.out.println(score1 <= score2); // 1이 2보다 작거나 같은가
    System.out.println(score1 == score2); // 1과 2가 같은가
    System.out.println(score1 != score2); // 1과 2가 다른가

  }
}
